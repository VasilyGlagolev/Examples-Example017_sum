// Задача 29: Напишите программу, которая задаёт массив из m элементов и выводит их на экран.
// 1, 2, 5, 7, 19 -> [1, 2, 5, 7, 19]
// 6, 1, 33 -> [6, 1, 33]

using System;

class Program {
    static void Main(string[] args) {
        // задаем массив из m элементов
        int m = 8;
        int[] arr = new int[m];

        // выводим массив на экран
        Console.Write("[");
        for (int i = 0; i < m; i++) {
            Console.Write(arr[i]);
            if (i < m - 1) {
                Console.Write(", ");
            }
        }
        Console.WriteLine("]");
        
        // позволяем пользователю задать количество элементов и значения в массиве
        Console.Write("Введите количество элементов в массиве: ");
        m = int.Parse(Console.ReadLine());
        arr = new int[m];
        Console.WriteLine("Введите элементы массива:");
        for (int i = 0; i < m; i++) {
            Console.Write("arr[{0}] = ", i);
            arr[i] = int.Parse(Console.ReadLine());
        }

        // выводим массив на экран
        Console.Write("[");
        for (int i = 0; i < m; i++) {
            Console.Write(arr[i]);
            if (i < m - 1) {
                Console.Write(", ");
            }
        }
        Console.WriteLine("]");
    }
}


// В этом примере мы сначала запрашиваем у пользователя количество элементов в массиве. 
// Затем мы создаем массив array размером m. 
// Далее мы запускаем цикл for, который будет запрашивать у пользователя каждый элемент массива
// и сохранять его в соответствующей ячейке массива. 
// После того, как все элементы массива были заданы, мы выводим массив на экран с помощью цикла for.